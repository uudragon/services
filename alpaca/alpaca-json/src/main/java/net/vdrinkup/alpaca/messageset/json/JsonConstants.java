package net.vdrinkup.alpaca.messageset.json;

/**
 * JSON常量类
 * <p>
 * 定义JSON所使用的特定字符常量
 * </p>
 * @author pluto.bing.liu
 * Date 2014-2-26
 */
public class JsonConstants {
	
	/**
	 * 双引号
	 */
	public static final int D_QUOTATION_MARK = 0x22;
	/**
	 * 单引号
	 */
	public static final int QUOTATION_MARK = 0x27;
	/**
	 * 左大括号
	 */
	public static final int L_BRACES = 0x7b;
	/**
	 * 右大括号
	 */
	public static final int R_BRACES = 0x7d;
	/**
	 * 左中括号
	 */
	public static final int L_BRACKET = 0x5b;
	/**
	 * 右中括号
	 */
	public static final int R_BRACKET = 0x5d;
	/**
	 * 逗号
	 */
	public static final int COMMA = 0x2c;
	/**
	 * 回车
	 */
	public static final int CR = 0xd;
	/**
	 * 换行
	 */
	public static final int LF = 0xa;
	/**
	 * 空格
	 */
	public static final int WHITESPACE = 0x20;
	/**
	 * 冒号
	 */
	public static final int COLON = 0x3a;
	/**
	 * BackSlash
	 */
	public static final int BACKSLASH = 0x5c;

}
